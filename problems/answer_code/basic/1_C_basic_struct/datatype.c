#include <stdio.h>

int main() {
    int a;  // 정수형 변수 : 4byte, 2^32 만큼의 수 표현 가능 : -2^31 ~ 2^31 -1, 약 ±21억
    long long b; // 정수형 변수 : 8byte, 2^64 만큼의 수 표현 가능 : -2^63 ~ 2^63 -1, 약 ±922경
    char c;  // 문자형 변수 : 1byte, 2^7 만큼 수 표현 가능 : 0 ~ 2^7-1, 0 ~ 127
    float d; // 실수형 변수 : 4byte, 정수부
    double e;
}